package com;

public class SimpleCalcuService {
	public int Add(int a, int b) {
		return a+b;    
	}
	public int Sub(int a, int b) {
		return a-b;
	}
	public int Mul(int a, int b) {
		return a*b;
	}
	public int Div(int a, int b) {
		return a/b;    
	}
	public int Rem(int a, int b) {
		return a%b;	
	}
}
